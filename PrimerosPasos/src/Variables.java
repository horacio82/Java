
public class Variables {

	public static void main(String[] args) {
	//Curso Java. Estructuras principales III. Declaración variables Eclipse Video 6
	//https://youtu.be/EUCmFmtX26I	
		
		byte edad; //declaración de varaiable
		
		edad=35;	//iniciación de variable
		
		/*La siguiente instrucción imprime en la consola (System.out) 
		 * el valor de la variable edad
		 */
		System.out.println(edad);
		
		//byte edad=75; (declaración e iniciación en una única línea)
		
		edad=75; //modifica la variable edad
		
		System.out.println(edad);
	}

}


